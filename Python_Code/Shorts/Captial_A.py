import turtle

t = turtle.Turtle()

t.pen()

t.pensize(20)

t.pencolor("red")

t.left(60)

t.forward(300)

t.right(120)

t.forward(300)

t.penup()

t.right(180)

t.forward(150)

t.pendown()

t.left(60)

t.forward(150)

turtle.done()


